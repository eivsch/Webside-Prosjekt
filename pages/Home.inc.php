		<h1 class="historie">Historie</h1>
		<h3>Begynnelsen</h3>
		<p>Til tross for stor oppmerksomhet de siste årene, er 3D-printing ikke noe nytt fenomen. Faktisk kan et bli sporet tilbake til 1976, året da inkjet-printeren ble oppfunnet. I 1984 hadde utvikling med
		   inkjet-konseptet ført til at printing-teknologien med blekk utviklet seg til printing-teknologien med materialer.
		   Denne teknologien kalles stereolitografi, og ble oppfunnet av Charles Hull, som siden skulle være med på å grunnlegge
		   selskapet 3D Systems. Stereolitografi tillater at man kan skape et tredimensjonalt objekt fra digitale data. Allerede da
		   forsto man at dette kunne brukes til å teste design før man investerte i produksjon i storstilt skala.</p>
		<p>I 1992 ble den første SLA(StereoLitografiske Apparatus)-maskinen skapt. I denne maskinens prosess blir fotopolymer
		   solide ved bruk av UV-laser. Dette gjør at tredimensjonale deler kan lages lag på lag. Selv om metoden ikke er perfekt,
		   gjør dette at svært kompliserte deler kan lages på mye kortere tid.
		   Det første modifiserte organet ble implantert i en pasient i 1999. En mann får en silikonisert urinblære ved bruk av
		   en 3D-syntetisert stillas dusjet i pasientens egne celler. Dette åpner for nye metoder å modifisere og lage nye organer på,
		   inkludert å 3D-printe dem. Siden de blir laget med celler fra pasientene, er det så og si ingen risk for avvisnig fra kroppen.</p>


		<h3>I nyere tid</h3>
		<p>I 2002 klarte forskere å lage en fullt funksjonerende nyre i et dyr. Dette ledet til forskning på regenererende medisin, som
		   har som mål å kunne "printe" organer og vev med 3D-printing. Dr. Adrian Bowyer grunnlegger RepRap i 2005 for å kunne lage
		   en 3D-printer som lager de fleste av sine egne komponenter. Ideen er at alle skal kunne ha en 3D-printer for å kunne lage sine
		   egne produkter på egen hånd. Den første SLS(Selektiv LaserSintring)-maskinen blir satt i bruk i 2006. Den bruker en laser for å koble
		   materialer til 3D-produkter. Dette gjennombruddet fører til massetilpasning og forespørselsproduksjon av industrideler, og siden,
		   proteser.</p>
		<p>Det samme året lager firmaet Objet en maskin som klarer å 3D-printe i flere forskjellige materialer, inkludert polymer og
		   elastomer. Den nye teknologien gjør at deler kan lages med forskjellige tettheter og materialer. 2008 blir et stort år for 3D-printing,
		   da en rekke nyvinninger kommer til: RepRap lager Darwin, en 3D-printer som klarer å replikere seg selv. Shapeways skaper et
		   nytt fellesskap for kunstnere, arkitekter og designere som ønsker å gjøre deres 3D-verk til fysiske objekter på en billig måte.
		   Og den første fungerende, fullstendig 3D-printede protesen blir tatt i bruk.</p>
		<p>I 2009 blir den første blodcellen 3D-printet, og selvbyggingspakker med 3D-printere blir lagt ut for salg.</p>
		<p>Så, i 2011 kommer en rekke gjennombrudd. Det første 3D-printede robotflyet, bilen og gull- og sølvprodukter blir lagd. Det nyeste
		   gjennombruddet kom med implanteringen av den første 3D-printede kunstige kjeven i 2012.</p>
