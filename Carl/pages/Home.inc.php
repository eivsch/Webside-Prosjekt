		<h2>Historie</h2>
		<h3>Begynnelsen</h3>
		<p>3D-printing kan bli sporet tilbake til 1976, året da inkjet-printeren ble oppfunnet. I 1984 hadde utvikling med
		   inkjet-konseptet ført til at printing-teknologien med blekk utviklet seg til printing-teknologien med materialer.
		   Denne teknologien kalles stereolitografi, og ble oppfunnet av Charles Hull, som siden skulle være med på å grunnlegge
		   selskapet 3D Systems. Stereolitografi tillater at man kan skape et tredimensjonalt objekt fra digitale data. Allerede da
		   forsto man at dette kunne brukes til å teste design før man investerte i produksjon i storstilt skala.</p>
		<p>I 1992 ble den første SLA(StereoLitografiske Apparatus)-maskinen skapt. I denne maskinens prosess blir fotopolymer
		   solide ved bruk av UV-laser. Dette gjør at tredimensjonale deler kan lages lag på lag. Selv om metoden ikke er perfekt,
		   gjør dette at svært kompliserte deler kan lages på mye kortere tid.
		   Det første modifiserte organet ble implantert i en pasient i 1999. En mann får en silikonisert urinblære ved bruk av
		   en 3D-syntetisert stillas dusjet i pasientens egne celler. Dette åpner for nye metoder å modifisere og lage nye organer på,
		   inkludert å 3D-printe dem. Siden de blir laget med celler fra pasientene, er det så å si ingen risk for avvisnig fra kroppen.</p>
